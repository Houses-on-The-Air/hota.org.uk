document.addEventListener('DOMContentLoaded', function() {
    // Add click-to-copy functionality to code blocks
    document.querySelectorAll('pre code').forEach(function(block) {
        block.addEventListener('click', function() {
            const textToCopy = block.innerText;
            navigator.clipboard.writeText(textToCopy).then(function() {
                M.toast({html: 'Code copied to clipboard!', classes: 'rounded'});
            }, function(err) {
                M.toast({html: 'Could not copy code: ' + err, classes: 'rounded red'});
            });
        });

        // Add click indicator
        block.style.cursor = 'pointer';
        block.setAttribute('title', 'Click to copy');
    });
});
