    <h2>Getting an Amateur Radio Licence in the UK</h2>
    <p>Last updated: <?= date('F j, Y'); ?></p>

    <h3>Step 1: Understand the Licence Levels</h3>
    <p>In the UK, amateur radio licences are issued by Ofcom and come in three levels:</p>
    <ul>
        <li><strong>Foundation Licence:</strong> Entry-level licence with limited power and frequency privileges.</li>
        <li><strong>Intermediate Licence:</strong> Grants more power and privileges, requiring a practical assessment.</li>
        <li><strong>Full Licence:</strong> The highest level, with full operating privileges and international recognition.</li>
    </ul>

    <h3>Step 2: Study for the Exam</h3>
    <p>To obtain a licence, you must pass an exam. Here’s how to prepare:</p>
    <ul>
        <li>Get the official study book from the <a href="https://rsgb.org/main/clubs-training/training-resources/" rel="nofollow">RSGB</a>.</li>
        <li>Join a local amateur radio club for support and training.</li>
        <li>Use online resources like mock exams and video tutorials.</li>
        <li>Practice using amateur radio equipment if possible.</li>
    </ul>

    <h3>Step 3: Book and Take the Exam</h3>
    <p>Once you feel ready, you can book your exam through the Radio Society of Great Britain (RSGB). The exam can be taken online or at a registered test centre. Exams consist of multiple-choice questions and, for Intermediate and Full licences, practical assessments.</p>

    <h3>Step 4: Receive Your Callsign and Licence</h3>
    <p>After passing the exam:</p>
    <ul>
        <li>You will receive a certificate confirming your pass.</li>
        <li>Register with Ofcom’s licencing portal to obtain your callsign.</li>
        <li>Your Foundation callsign will typically begin with the prefix <strong>M7</strong>.</li>
    </ul>

    <h3>Step 5: Get Your Equipment and Get on the Air</h3>
    <p>Now that you’re licenced, you need the right equipment:</p>
    <ul>
        <li><strong>Transceiver:</strong> A radio capable of operating on the allowed bands.</li>
        <li><strong>Antenna:</strong> Suitable for your chosen frequencies.</li>
        <li><strong>Power Supply:</strong> Ensuring safe operation.</li>
        <li><strong>Logging Software (Optional):</strong> To keep track of contacts.</li>
    </ul>
    <p>Start by listening to bands and calling CQ to make contacts. Join local nets or digital modes to gain experience.</p>

    <h3>Step 6: Upgrade Your Licence</h3>
    <p>If you wish to gain more privileges, you can progress to Intermediate and Full licences by taking further exams.</p>

