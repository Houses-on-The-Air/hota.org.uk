    <h2>Glossary of Amateur Radio Terms</h2>
    <p>Last updated: <?= date('F j, Y'); ?></p>
    <p>Expand your knowledge of amateur radio with this glossary of common terms and acronyms used in the hobby.</p>

    <h3>A</h3>
    <dl>
        <dt>Amateur Radio</dt>
        <dd>Amateur radio, also known as ham radio, is a popular hobby and service that allows licensed operators to communicate with each other using various radio frequencies. Amateur radio operators use a variety of modes and bands to communicate locally, regionally, and globally.</dd>
        <dt>Antenna</dt>
        <dd>An antenna is a device used to transmit and receive radio waves. It is an essential component of any radio communication system.</dd>
    </dl>

    <h3>B</h3>
    <dl>
        <dt>Band</dt>
        <dd>In amateur radio, a band refers to a range of frequencies that are allocated for specific types of communication. Different bands are used for different modes of communication, such as voice, Morse code, and data.</dd>
        <dt>Beacon</dt>
        <dd>A beacon is a radio transmitter that sends out a continuous or periodic signal for the purpose of navigation or location finding.</dd>
    </dl>

    <h3>C</h3>
    <dl>
        <dt>Callsign</dt>
        <dd>A callsign is a unique identifier assigned to an amateur radio operator by the licensing authority. Callsigns are used to identify operators during radio communications and are typically a combination of letters and numbers.</dd>
        <dt>CQ</dt>
        <dd>CQ is a general call to all amateur radio operators, indicating that the sender is seeking any station to respond.</dd>
    </dl>

    <h3>D</h3>
    <dl>
        <dt>Digital Modes</dt>
        <dd>Digital modes are methods of transmitting data over radio waves using digital signals. Digital modes allow for more efficient and reliable communication compared to traditional analog modes.</dd>
        <dt>Dipole Antenna</dt>
        <dd>A dipole antenna is a simple antenna consisting of two conductive elements, typically used for HF and VHF communication.</dd>
    </dl>

    <h3>E</h3>
    <dl>
        <dt>Emergency Communications</dt>
        <dd>Emergency communications are radio communications used during emergencies and disasters to provide critical information and support to affected areas. Amateur radio operators often play a key role in emergency communications by providing backup communication services when other systems fail.</dd>
        <dt>Elmer</dt>
        <dd>An Elmer is an experienced amateur radio operator who mentors and helps new operators learn the ropes of the hobby.</dd>
    </dl>

    <h3>F</h3>
    <dl>
        <dt>Frequency</dt>
        <dd>Frequency refers to the number of cycles per second of a radio wave. In amateur radio, frequencies are measured in hertz (Hz) and are used to determine the operating frequency of a radio station.</dd>
        <dt>Field Day</dt>
        <dd>Field Day is an annual event in amateur radio where operators set up portable stations and operate under emergency conditions to practice and demonstrate their emergency communication capabilities.</dd>
    </dl>

    <h3>G</h3>
    <dl>
        <dt>General Class License</dt>
        <dd>The General Class license is a license class in amateur radio that grants operators additional operating privileges on a wider range of frequencies and modes compared to the entry-level Technician Class license.</dd>
        <dt>Ground Plane</dt>
        <dd>A ground plane is a conductive surface that serves as a reference point for an antenna, improving its performance and radiation pattern.</dd>
    </dl>

    <h3>H</h3>
    <dl>
        <dt>HF (High Frequency)</dt>
        <dd>HF refers to the range of radio frequencies from 3 to 30 MHz. HF bands are commonly used for long-distance communication because they can reflect off the ionosphere and travel great distances.</dd>
        <dt>Hamfest</dt>
        <dd>A hamfest is a gathering of amateur radio enthusiasts, typically featuring swap meets, vendor exhibits, and educational sessions.</dd>
    </dl>

    <h3>I</h3>
    <dl>
        <dt>Ionosphere</dt>
        <dd>The ionosphere is a layer of the Earth's atmosphere that is ionized by solar radiation. It plays a crucial role in radio communication by reflecting radio waves back to Earth, allowing for long-distance communication.</dd>
        <dt>Impedance</dt>
        <dd>Impedance is the measure of opposition that a circuit presents to the flow of alternating current. It is an important factor in antenna and transmission line design.</dd>
    </dl>

    <h3>J</h3>
    <dl>
        <dt>J-Pole Antenna</dt>
        <dd>A J-Pole antenna is a type of omnidirectional antenna commonly used in amateur radio. It is simple to construct and provides good performance for VHF and UHF frequencies.</dd>
        <dt>JT65</dt>
        <dd>JT65 is a digital mode used for weak signal communication, particularly in moonbounce (EME) and long-distance HF communication.</dd>
    </dl>

    <h3>K</h3>
    <dl>
        <dt>Keyer</dt>
        <dd>A keyer is an electronic device used in Morse code communication to automatically generate dots and dashes. It helps operators send Morse code more accurately and efficiently.</dd>
        <dt>Kilohertz (kHz)</dt>
        <dd>Kilohertz is a unit of frequency equal to 1,000 hertz. It is commonly used to specify frequencies in the lower radio spectrum.</dd>
    </dl>

    <h3>L</h3>
    <dl>
        <dt>License</dt>
        <dd>An amateur radio license is an authorization granted by a government authority that allows an individual to operate amateur radio equipment. Different license classes grant different operating privileges.</dd>
        <dt>Logbook</dt>
        <dd>A logbook is a record of radio contacts made by an amateur radio operator, typically including details such as date, time, frequency, and callsign of the contacted station.</dd>
    </dl>

    <h3>M</h3>
    <dl>
        <dt>Morse Code</dt>
        <dd>Morse code is a method of encoding text characters as sequences of dots and dashes. It is one of the oldest forms of digital communication and is still used by amateur radio operators today.</dd>
        <dt>Modulation</dt>
        <dd>Modulation is the process of varying a carrier signal to transmit information. Common types of modulation include amplitude modulation (AM), frequency modulation (FM), and phase modulation (PM).</dd>
    </dl>

    <h3>N</h3>
    <dl>
        <dt>Net</dt>
        <dd>A net is a scheduled on-air meeting of amateur radio operators. Nets are used for various purposes, including emergency communication drills, information sharing, and social interaction.</dd>
        <dt>Noise</dt>
        <dd>Noise refers to unwanted signals or interference that can affect the quality of radio communication. It can be caused by natural sources, such as lightning, or man-made sources, such as electrical equipment.</dd>
    </dl>

    <h3>O</h3>
    <dl>
        <dt>Oscillator</dt>
        <dd>An oscillator is an electronic circuit that generates a continuous, oscillating signal. Oscillators are used in radio transmitters and receivers to generate and process radio frequencies.</dd>
        <dt>Omnidirectional Antenna</dt>
        <dd>An omnidirectional antenna is an antenna that radiates or receives signals equally well in all directions. It is commonly used for local communication.</dd>
    </dl>

    <h3>P</h3>
    <dl>
        <dt>Propagation</dt>
        <dd>Propagation refers to the behavior of radio waves as they travel through the Earth's atmosphere. Understanding propagation is essential for predicting how radio signals will travel and where they will be received.</dd>
        <dt>Phonetic Alphabet</dt>
        <dd>The phonetic alphabet is a standardized set of words used to represent letters in voice communication, improving clarity and reducing misunderstandings. For example, "A" is represented as "Alpha," "B" as "Bravo," and so on.</dd>
    </dl>

    <h3>Q</h3>
    <dl>
        <dt>QSL Card</dt>
        <dd>A QSL card is a written confirmation of a radio contact between two amateur radio operators. QSL cards are often exchanged as a form of acknowledgment and as a collectible item.</dd>
        <dt>QRP</dt>
        <dd>QRP refers to low-power operation in amateur radio, typically using 5 watts or less. It is a popular challenge among operators to make long-distance contacts with minimal power.</dd>
    </dl>

    <h3>R</h3>
    <dl>
        <dt>Repeater</dt>
        <dd>A repeater is a radio station that receives a signal on one frequency and retransmits it on another frequency. Repeaters extend the range of communication by allowing signals to be received and retransmitted over greater distances.</dd>
        <dt>RIT (Receiver Incremental Tuning)</dt>
        <dd>RIT is a feature on some radios that allows the operator to adjust the receive frequency without changing the transmit frequency, useful for fine-tuning during communication.</dd>
    </dl>

    <h3>S</h3>
    <dl>
        <dt>SSB (Single Sideband)</dt>
        <dd>SSB is a mode of voice communication that uses less bandwidth and power compared to traditional AM (Amplitude Modulation). It is commonly used in amateur radio for long-distance communication.</dd>
        <dt>SWR (Standing Wave Ratio)</dt>
        <dd>SWR is a measure of the efficiency of power transfer between a transmitter and an antenna. A low SWR indicates efficient power transfer, while a high SWR indicates potential signal loss and equipment damage.</dd>
    </dl>

    <h3>T</h3>
    <dl>
        <dt>Technician Class License</dt>
        <dd>The Technician Class license is the entry-level license in amateur radio. It grants operators access to certain VHF and UHF frequencies and limited HF privileges.</dd>
        <dt>Transceiver</dt>
        <dd>A transceiver is a device that combines a transmitter and a receiver in a single unit, commonly used in amateur radio stations.</dd>
    </dl>

    <h3>U</h3>
    <dl>
        <dt>UHF (Ultra High Frequency)</dt>
        <dd>UHF refers to the range of radio frequencies from 300 MHz to 3 GHz. UHF bands are commonly used for short-distance communication, such as local repeaters and satellite communication.</dd>
        <dt>UTC (Coordinated Universal Time)</dt>
        <dd>UTC is the primary time standard by which the world regulates clocks and time. It is used in amateur radio to log contacts and schedule events across different time zones.</dd>
    </dl>

    <h3>V</h3>
    <dl>
        <dt>VHF (Very High Frequency)</dt>
        <dd>VHF refers to the range of radio frequencies from 30 MHz to 300 MHz. VHF bands are commonly used for medium-distance communication, such as local repeaters and line-of-sight communication.</dd>
        <dt>Vertical Antenna</dt>
        <dd>A vertical antenna is an antenna with a vertical orientation, often used for HF and VHF communication. It provides omnidirectional coverage and is relatively easy to install.</dd>
    </dl>

    <h3>W</h3>
    <dl>
        <dt>Watt</dt>
        <dd>A watt is a unit of power used to measure the output power of a radio transmitter. Higher wattage allows for stronger signals and greater communication range.</dd>
        <dt>Worked All States (WAS)</dt>
        <dd>WAS is an amateur radio award given to operators who have made confirmed contacts with stations in all 50 U.S. states.</dd>
    </dl>

    <h3>X</h3>
    <dl>
        <dt>X-Band</dt>
        <dd>X-Band refers to a segment of the microwave radio spectrum in the frequency range of 8 to 12 GHz. It is used for radar, satellite communication, and other applications.</dd>
        <dt>XIT (Transmitter Incremental Tuning)</dt>
        <dd>XIT is a feature on some radios that allows the operator to adjust the transmit frequency without changing the receive frequency, useful for fine-tuning during communication.</dd>
    </dl>

    <h3>Y</h3>
    <dl>
        <dt>Yagi Antenna</dt>
        <dd>A Yagi antenna is a directional antenna commonly used in amateur radio. It consists of multiple elements arranged in a line and provides high gain and directivity.</dd>
        <dt>YL (Young Lady)</dt>
        <dd>YL is a term used in amateur radio to refer to a female operator, regardless of age.</dd>
    </dl>

    <h3>Z</h3>
    <dl>
        <dt>Zero Beat</dt>
        <dd>Zero beat refers to the point at which two radio signals are exactly in phase and have the same frequency. It is used in Morse code communication to ensure accurate tuning.</dd>
        <dt>Zener Diode</dt>
        <dd>A Zener diode is a type of diode that allows current to flow in the reverse direction when a specific voltage is reached. It is commonly used in voltage regulation circuits.</dd>
    </dl>

