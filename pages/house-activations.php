                        <li>Power source (battery, generator, etc.)</li>
                        <li>Antennas and supports</li>
                        <li>Logging equipment (paper log, laptop, tablet)</li>
                        <li>Coaxial cable and adapters</li>
                        <li>Headphones</li>
                        <li>Weather protection (if operating outdoors)</li>
                        <li>Seating and table if needed</li>
                        <li>Documentation of permission to operate</li>
                        <li>House information for your log</li>
