                    <h5>Include Address Information</h5>
                    <p>For each contact, make sure to exchange:</p>
                    <ul class="browser-default">
                        <li>Signal reports</li>
                        <li>Your full house address</li>
                        <li>Name and QTH information</li>
                    </ul>
                </div>
