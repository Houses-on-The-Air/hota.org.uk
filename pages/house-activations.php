                    <ol>
                        <li><strong>Choose your location</strong> - Select a house that's accessible and where you have permission to operate</li>
                        <li><strong>Check the address</strong> - Make sure you have the complete address (including street number, street name, town/city, postal code)</li>
                        <li><strong>Plan your setup</strong> - Decide on equipment, power source, and antenna options</li>
                        <li><strong>Announce your activation</strong> - Post your plans on the HOTA Discord or forum to generate interest</li>
                        <li><strong>Check propagation</strong> - Review band conditions to maximize your chances of making contacts</li>
                    </ol>
                </div>
                <div class="col s12 m6">
