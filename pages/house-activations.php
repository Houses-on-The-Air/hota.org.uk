            <span class="card-title">Getting Started</span>
            <p>Activating a house means setting up your amateur radio station at a house location and making contacts from there. Here's what you need to know to begin:</p>

            <h4>Basic Requirements</h4>
            <ul class="browser-default">
                <li>A valid amateur radio license</li>
                <li>Portable radio equipment</li>
                <li>Permission to operate at the location</li>
                <li>A log for recording contacts</li>
                <li>A method for recording the full address</li>
            </ul>

            <div class="card-panel blue-grey lighten-5">
                <h5><i class="material-icons left">info</i>What Counts as a House?</h5>
                <p>For HOTA purposes, a "house" is defined as any residential structure with a unique address where people live or have lived. This includes:</p>
                <ul class="browser-default">
                    <li>Single-family homes</li>
                    <li>Apartments and flats</li>
                    <li>Cottages and cabins</li>
                    <li>Historic homes and manors</li>
                    <li>Residential structures on the register of historic places</li>
                </ul>
                <p>Non-residential buildings (businesses, shops, etc.) don't qualify for HOTA.</p>
            </div>
