<script>
document.addEventListener('DOMContentLoaded', function() {
    // Initialize select elements
    var selects = document.querySelectorAll('select');
    M.FormSelect.init(selects);

    // Initialize expandable collapsible elements
    var collapsibleElements = document.querySelectorAll('.collapsible.expandable');
    M.Collapsible.init(collapsibleElements, {
        accordion: false
    });

    // Show/hide "other role" field based on selection
    document.getElementById('role_interest').addEventListener('change', function() {
        var otherRoleField = document.getElementById('other_role').parentElement;
        if (this.value === 'Other') {
            otherRoleField.style.display = 'block';
        } else {
            otherRoleField.style.display = 'none';
            document.getElementById('other_role').value = '';
        }
    });

    // Form validation
    document.getElementById('volunteer-form').addEventListener('submit', function(e) {
        var roleInterest = document.getElementById('role_interest').value;
        var otherRole = document.getElementById('other_role').value;

        if (roleInterest === 'Other' && !otherRole) {
            e.preventDefault();
            M.toast({html: 'Please specify your role interest'});
            return false;
        }
    });
});
</script>
