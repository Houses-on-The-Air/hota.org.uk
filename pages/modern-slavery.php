    <h2>Modern Slavery Statement</h2>
    <p>Last updated: <?= date('F j, Y'); ?></p>

    <p>This statement is made pursuant to section 54 of the Modern Slavery Act 2015 and sets out the steps that HOTA is taking to ensure that modern slavery and human trafficking are not taking place in our supply chains or in any part of our business.</p>

    <h3>Our Organisation</h3>
    <p>HOTA is a non-profit organisation that promotes amateur radio and provides resources for amateur radio operators. Our mission is to foster camaraderie among radio amateurs, provide educational resources, and support the growth of amateur radio worldwide.</p>

    <h3>Our Supply Chains</h3>
    <p>Our supply chains include the procurement of goods and services for our operations, such as equipment, software, and professional services. We are committed to ensuring that our suppliers adhere to the highest standards of ethical conduct and comply with all applicable laws and regulations.</p>

    <h3>Our Policies</h3>
    <p>We are committed to acting ethically and with integrity in all our business relationships and to implementing and enforcing effective systems and controls to prevent modern slavery and human trafficking in our operations and supply chains. Our policies reflect our commitment to acting ethically and with integrity in all our business relationships and to implementing and enforcing effective systems and controls to prevent modern slavery and human trafficking in our operations and supply chains.</p>

    <h3>Due Diligence Processes</h3>
    <p>We conduct due diligence on all suppliers before entering into a business relationship with them and regularly review our existing suppliers to ensure compliance with our policies and standards. We require our suppliers to comply with all applicable laws and regulations, including those related to modern slavery and human trafficking.</p>

    <h3>Training</h3>
    <p>We provide training to our employees on the risks of modern slavery and human trafficking and how to identify and report any concerns. We also provide training to our suppliers on their responsibilities under the Modern Slavery Act and the steps they can take to prevent modern slavery and human trafficking in their operations and supply chains.</p>

    <h3>Reporting</h3>
    <p>We encourage our employees, suppliers, and other stakeholders to report any concerns related to modern slavery and human trafficking. We have established procedures for reporting and investigating such concerns and take appropriate action to address any violations of our policies and standards.</p>

    <h3>Approval</h3>
    <p>This statement has been approved by the Board of Directors of HOTA and will be reviewed annually to ensure its continued effectiveness and compliance with the Modern Slavery Act 2015.</p>

    <h3>Contact Us</h3>
    <p>If you have any questions or concerns about our modern slavery statement or wish to report any concerns related to us or our suppliers, please contact us at <a href="mailto:info@hota.org.uk">info@hota.org.uk</a>.</p>

    <h3>Changes to This Statement</h3>
    <p>We may update this statement from time to time. Any changes will be posted on this page, and where appropriate, notified to you by email.</p>

