            <p>Modern slavery encompasses slavery, servitude, human trafficking, and forced labor. HOTA has a zero-tolerance approach to any form of modern slavery. We are committed to acting ethically and with integrity and transparency in all business dealings and to putting effective systems and controls in place to safeguard against any form of modern slavery taking place within our organization or our supply chain.</p>
