    <div class="card mt-3">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">fact_check</i>Due Diligence Processes</span>

            <h4>Supplier Assessment and Monitoring</h4>
            <p>As part of our efforts to identify and mitigate risks, we undertake the following due diligence processes:</p>

            <ul class="browser-default">
                <li>Mapping our supply chain to assess product or service category risks</li>
                <li>Evaluating all new suppliers using our supplier questionnaire which covers labor practices, health and safety, environmental impacts, and business ethics</li>
                <li>Requiring all suppliers to confirm compliance with the Modern Slavery Act 2015</li>
                <li>Conducting periodic reviews of existing suppliers, with frequency determined by risk assessment</li>
                <li>Prioritizing suppliers that have their own published modern slavery statements and ethical policies</li>
            </ul>

            <h4>Risk Assessment</h4>
            <p>We assess risk based on:</p>
            <ul class="browser-default">
                <li>Geographic risk factors, considering the prevalence of modern slavery in different countries</li>
                <li>Industry sector risk, with particular attention to manufacturing and textiles</li>
                <li>The nature of the business relationship and our leverage with suppliers</li>
                <li>Transaction risk, including payment methods and financial transparency</li>
            </ul>
        </div>
    </div>

