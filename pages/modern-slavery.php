            <ul class="browser-default">
                <li>Is governed by a volunteer committee based primarily in the United Kingdom</li>
                <li>Operates primarily through digital channels and remote coordination</li>
                <li>Produces limited physical goods, primarily certificates and promotional materials</li>
                <li>Works with a small number of suppliers for website hosting, merchandise production, and certificate printing</li>
                <li>Has no employees but is supported by a dedicated team of volunteers worldwide</li>
            </ul>

            <p>While our organization is relatively small and primarily volunteer-driven, we recognize the importance of combating modern slavery and ensuring ethical practices throughout our operations and supply chains.</p>
