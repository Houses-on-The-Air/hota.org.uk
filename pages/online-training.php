    <!-- FAQ Section -->
    <div class="card">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">help</i>Frequently Asked Questions</span>

            <ul class="collapsible expandable">
                <li>
                    <div class="collapsible-header"><i class="material-icons">help_outline</i>How long does it take to prepare for each licence level?</div>
                    <div class="collapsible-body">
                        <p>Study times vary based on individual background knowledge and study intensity:</p>
                        <ul class="browser-default">
                            <li><strong>Foundation:</strong> Typically 2-4 weeks of study (20-30 hours total)</li>
                            <li><strong>Intermediate:</strong> Around 1-2 months of study (40-60 hours total)</li>
                            <li><strong>Full:</strong> Usually 3-6 months of dedicated study (100+ hours)</li>
                        </ul>
                        <p>Many people with technical backgrounds progress more quickly, while those without prior electronics knowledge might need more time.</p>
                    </div>
                </li>
                <li>
                    <div class="collapsible-header"><i class="material-icons">help_outline</i>How much does it cost to get licensed?</div>
                    <div class="collapsible-body">
                        <p>The cost includes examination fees and study materials:</p>
                        <ul class="browser-default">
                            <li><strong>Foundation exam fee:</strong> £27.50</li>
                            <li><strong>Intermediate exam fee:</strong> £32.50</li>
                            <li><strong>Full exam fee:</strong> £37.50</li>
                            <li><strong>Study materials:</strong> £15-£30 per level (books)</li>
                        </ul>
                        <p>Additionally, free online resources can supplement or replace purchased materials. Some clubs may offer discounted or included study materials with their training courses.</p>
                    </div>
                </li>
                <li>
                    <div class="collapsible-header"><i class="material-icons">help_outline</i>Do I need mathematical skills for the exams?</div>
                    <div class="collapsible-body">
                        <p>Each licence level requires progressively more mathematical ability:</p>
                        <ul class="browser-default">
                            <li><strong>Foundation:</strong> Basic arithmetic and simple formulas</li>
                            <li><strong>Intermediate:</strong> More calculations including Ohm's Law and power calculations</li>
                            <li><strong>Full:</strong> More advanced calculations including reactance, resonance, decibels, and complex circuit analysis</li>
                        </ul>
                        <p>Study materials provide worked examples and practice questions to help you master the required calculations.</p>
                    </div>
                </li>
                <li>
                    <div class="collapsible-header"><i class="material-icons">help_outline</i>Do I need to know Morse code?</div>
                    <div class="collapsible-body">
                        <p>No, Morse code is no longer a requirement for any UK amateur radio licence level. It was phased out as a mandatory requirement in the early 2000s.</p>
                        <p>However, many amateur radio operators still learn and use Morse code (CW) as it's an effective mode of communication, especially in challenging conditions. It's a respected skill in the amateur radio community but completely optional.</p>
                    </div>
                </li>
                <li>
                    <div class="collapsible-header"><i class="material-icons">help_outline</i>What equipment do I need for the practical assessments?</div>
                    <div class="collapsible-body">
                        <p>Currently, due to COVID-19, the RSGB has modified the practical assessment requirements. Check the <a href="https://rsgb.org/main/clubs-training/tutor-resources/practical-assessments/" target="_blank" rel="nofollow noopener">RSGB website</a> for the most current information.</p>
                        <p>Traditionally:</p>
                        <ul class="browser-default">
                            <li><strong>Foundation:</strong> Demonstrate basic operation of radio equipment (usually provided at exam centres)</li>
                            <li><strong>Intermediate:</strong> Complete a small construction project and demonstrate its functionality</li>
                        </ul>
                        <p>The Full licence has no practical assessment component.</p>
                    </div>
                </li>
                <li>
                    <div class="collapsible-header"><i class="material-icons">help_outline</i>How soon after passing can I get on the air?</div>
                    <div class="collapsible-body">
                        <p>After passing your exam, the RSGB will notify Ofcom (usually within 1-3 business days). You'll then need to apply for your licence through the Ofcom portal, which is a quick online process.</p>
                        <p>Your callsign is typically issued within a few days after application. Once you have your official callsign, you can begin operating immediately. The whole process from exam passing to getting on air usually takes about a week, but can sometimes be completed in as little as 2-3 days.</p>
                    </div>
                </li>
            </ul>
        </div>
