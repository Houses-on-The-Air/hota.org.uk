    <div id="general-guidelines" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">list</i>General Operating Guidelines</span>

            <p>These principles apply to all HOTA operations, whether you're activating a location or hunting for contacts:</p>

            <div class="row">
                <div class="col s12 m6">
                    <h4><i class="material-icons left small blue-grey-text">verified_user</i>Legal Operation</h4>
                    <ul class="browser-default">
                        <li>Always operate within your licence privileges</li>
                        <li>Comply with local regulations and band plans</li>
                        <li>Respect power restrictions in your area</li>
                        <li>Identify your station according to regulations</li>
                        <li>Obtain proper permission for any location you operate from</li>
                    </ul>
                </div>
                <div class="col s12 m6">
                    <h4><i class="material-icons left small blue-grey-text">people</i>Courtesy</h4>
                    <ul class="browser-default">
                        <li>Be patient, especially with newcomers</li>
                        <li>Keep transmissions brief during busy periods</li>
                        <li>Listen before transmitting to avoid interrupting ongoing contacts</li>
                        <li>Give accurate signal reports</li>
                        <li>Thank stations for their contacts</li>
                    </ul>
                </div>
            </div>

            <div class="row">
                <div class="col s12 m6">
                    <h4><i class="material-icons left small blue-grey-text">settings_input_antenna</i>Good Practice</h4>
                    <ul class="browser-default">
                        <li>Use standard phonetics for clarity</li>
                        <li>Speak clearly and at a moderate pace</li>
                        <li>Exchange complete information (address, signal reports, etc.)</li>
                        <li>Log contacts accurately with correct time in UTC</li>
                        <li>Confirm contacts with a clear acknowledgment</li>
                    </ul>
                </div>
                <div class="col s12 m6">
                    <h4><i class="material-icons left small blue-grey-text">security</i>Safety</h4>
                    <ul class="browser-default">
                        <li>Ensure safe antenna installations</li>
                        <li>Be aware of RF exposure guidelines</li>
                        <li>Consider weather conditions when operating outdoors</li>
                        <li>Have emergency contact information available</li>
                        <li>Never compromise safety for a contact</li>
                    </ul>
                </div>
            </div>

            <div class="card-panel light-blue lighten-5 mt-3">
                <h5><i class="material-icons left">lightbulb</i>Pro Tip</h5>
                <p>When calling "CQ HOTA" (or responding to such calls), speak clearly and use standard phonetics. For example: "CQ HOTA, CQ HOTA, this is Mike Zero Alpha Bravo Charlie, Mike Zero Alpha Bravo Charlie calling CQ for Houses On The Air and standing by."</p>
            </div>
        </div>
