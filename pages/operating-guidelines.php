    <!-- Activator Guidelines -->
    <div id="activator-guidelines" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">home</i>Guidelines for Activators</span>

            <p>When activating a house location for HOTA, follow these specific guidelines:</p>

            <h4>Planning Your Activation</h4>
            <div class="row">
                <div class="col s12 m6">
                    <div class="card-panel">
                        <h5>Before Your Activation</h5>
                        <ol>
                            <li>Obtain proper permission for your location</li>
                            <li>Announce your activation in advance (Discord, social media)</li>
                            <li>Check propagation forecasts to plan bands and times</li>
                            <li>Prepare logging equipment and ensure batteries are charged</li>
                            <li>Have a detailed address ready to share with contacts</li>
                        </ol>
                    </div>
                </div>
                <div class="col s12 m6">
                    <div class="card-panel">
                        <h5>During Your Activation</h5>
                        <ol>
                            <li>Begin by listening to ensure the frequency is clear</li>
                            <li>Call "CQ HOTA" and identify your location clearly</li>
                            <li>Give your complete address with each contact</li>
                            <li>Keep a proper log with times, callsigns, and reports</li>
                            <li>If operating for extended periods, take regular breaks</li>
                        </ol>
                    </div>
                </div>
            </div>

            <h4>Effective Communication</h4>
            <div class="example-box">
                <h5>Example Activator Exchange</h5>
                <div class="example-dialog">
                    <p><strong>M0ABC:</strong> "CQ HOTA, CQ HOTA, this is Mike Zero Alpha Bravo Charlie calling from 123 High Street, London and standing by."</p>
                    <p><strong>G3XYZ:</strong> "M0ABC this is Golf Three X-Ray Yankee Zulu, Golf Three X-Ray Yankee Zulu calling."</p>
                    <p><strong>M0ABC:</strong> "G3XYZ this is M0ABC, good afternoon. You are 5 and 9 from 123 High Street, London SW1A 1AA. HOTA activation. Over."</p>
                    <p><strong>G3XYZ:</strong> "M0ABC this is G3XYZ. Thanks for the 5 and 9 report. You are also 5 and 9 here in Manchester. Thanks for the HOTA activation and the address. 73."</p>
                    <p><strong>M0ABC:</strong> "G3XYZ this is M0ABC. Thanks for the contact, 73 and QRZ for HOTA from 123 High Street, London."</p>
                </div>
            </div>

            <div class="card-panel deep-orange lighten-5 mt-3">
                <h5><i class="material-icons left">priority_high</i>Important</h5>
                <p>Always clearly communicate the complete address of your location in each contact. This is essential for the HOTA program as unique addresses are the basis for awards.</p>
