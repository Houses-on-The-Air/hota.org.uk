    <!-- Digital Operation -->
    <div id="digital-operation" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">computer</i>Digital Mode Operation</span>

            <p>Digital modes present unique challenges for HOTA operations, particularly for exchanging address information:</p>

            <div class="row">
                <div class="col s12 m6">
                    <h4>FT8/FT4 Operation</h4>
                    <p>These modes use standardized exchanges with limited characters, making address sharing difficult.</p>
                    <h5>Solutions:</h5>
                    <ol>
                        <li>Post your complete address on the HOTA Discord or spotting network before operating</li>
                        <li>Include a brief identifier in your free-text message (e.g., "HOTA 123HighSt")</li>
                        <li>Follow up digital contacts with full address information via:</li>
                        <ul class="browser-default">
                            <li>HOTA website lookup (upcoming feature)</li>
                            <li>Email confirmation</li>
                            <li>Discord direct message</li>
                            <li>QRZ.com or similar profile messages</li>
                        </ul>
                    </ol>
                </div>
                <div class="col s12 m6">
                    <h4>Other Digital Modes</h4>
                    <p>Modes like PSK31, RTTY, etc., allow for longer free-text exchanges.</p>
                    <h5>Best Practices:</h5>
                    <ol>
                        <li>Prepare macros with your complete address information</li>
                        <li>Example macro: "CQ HOTA HOTA DE [CALLSIGN] [CALLSIGN] OPERATING FROM [FULL ADDRESS] K"</li>
                        <li>Exchange address in fragments if necessary</li>
                        <li>Confirm receipt of complete address information</li>
                        <li>Log the contact immediately with address details</li>
                    </ol>
                </div>
            </div>

            <div class="card-panel light-blue lighten-5 mt-3">
                <h5><i class="material-icons left">lightbulb</i>Pro Tip</h5>
                <p>For FT8/FT4 operations, consider operating in "Fox/Hound" mode during busy activations. This allows you to manage multiple hunters efficiently while maintaining better control over the QSO flow.</p>
            </div>
        </div>
    </div>

    <!-- Log Keeping -->
    <div id="logkeeping" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">assignment</i>Log Keeping Guidelines</span>

            <p>Accurate log keeping is essential for HOTA operations to track unique addresses:</p>

            <div class="row">
                <div class="col s12 m6">
                    <h4>Essential Log Fields</h4>
                    <ul class="browser-default">
                        <li><strong>Date and Time (UTC)</strong> - When the contact occurred</li>
                        <li><strong>Callsign</strong> - Who you contacted</li>
                        <li><strong>Band/Frequency</strong> - Where the contact took place</li>
                        <li><strong>Mode</strong> - How you communicated (SSB, CW, FT8, etc.)</li>
                        <li><strong>Signal Reports</strong> - RST sent and received</li>
                        <li><strong>ADDRESS</strong> - Complete house address (critical for HOTA)</li>
                    </ul>
                </div>
                <div class="col s12 m6">
                    <h4>Log Submission Format</h4>
                    <p>HOTA requires logs in ADIF format with proper ADDRESS field:</p>
                    <pre><code>&lt;CALL:5&gt;G3XYZ &lt;QSO_DATE:8&gt;20250415 &lt;TIME_ON:6&gt;142500
&lt;BAND:3&gt;20m &lt;MODE:3&gt;SSB &lt;RST_SENT:3&gt;59 &lt;RST_RCVD:3&gt;57
&lt;ADDRESS:28&gt;123 High Street, London SW1A 1AA &lt;EOR&gt;</code></pre>
                    <p>See our <a href="?page=adif-guide">ADIF Format Guide</a> for detailed instructions.</p>
                </div>
            </div>

            <h4>Using Logging Software</h4>
            <div class="row">
                <div class="col s12">
                    <p>Most logging software supports ADIF export, but you may need to configure it properly:</p>
                    <ol>
                        <li>Ensure your software has a field for ADDRESS or QTH</li>
                        <li>Enter the complete address in this field for each contact</li>
                        <li>When exporting, verify that ADDRESS fields are included in the ADIF file</li>
                        <li>Test your export with a few contacts before a major activation</li>
                        <li>See our <a href="?page=logging-software">Logging Software Guide</a> for specific software instructions</li>
                    </ol>
                </div>
            </div>

            <div class="card-panel deep-orange lighten-5 mt-3">
                <h5><i class="material-icons left">priority_high</i>Critical Reminder</h5>
                <p>The ADDRESS field is absolutely essential for HOTA award tracking. Without it, your contacts won't count towards HOTA awards. Always verify your logs contain this field before submission.</p>
            </div>
        </div>
    </div>

    <!-- Operating Etiquette -->
    <div id="etiquette" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">thumb_up</i>Operating Etiquette</span>

            <p>Good operating etiquette ensures a positive experience for all HOTA participants:</p>

            <div class="row">
                <div class="col s12 m6">
                    <h4>Do's</h4>
                    <ul class="browser-default good-practices">
                        <li>Listen before transmitting</li>
                        <li>Use standard phonetics for clarity</li>
                        <li>Keep transmissions brief during busy periods</li>
                        <li>Thank operators for contacts</li>
                        <li>Give honest signal reports</li>
                        <li>Be patient with newer operators</li>
                        <li>Share frequencies during contests</li>
                        <li>Announce when you're finishing an activation</li>
                        <li>Follow band plans and regulations</li>
                        <li>Help others understand the HOTA program</li>
                    </ul>
                </div>
                <div class="col s12 m6">
                    <h4>Don'ts</h4>
                    <ul class="browser-default avoid-practices">
                        <li>Transmit without listening first</li>
                        <li>Make excessively long calls</li>
                        <li>Interrupt ongoing QSOs</li>
                        <li>Give inaccurate signal reports</li>
                        <li>Criticize other operators publicly</li>
                        <li>"Hog" a frequency unnecessarily</li>
                        <li>Use non-standard abbreviations</li>
                        <li>Operate outside your license privileges</li>
                        <li>Rush exchanges (addresses need clarity)</li>
                        <li>Operate from houses without permission</li>
                    </ul>
                </div>
            </div>

            <h4>Handling Difficult Situations</h4>
            <div class="row">
                <div class="col s12 m6">
                    <h5>When Frequencies Are Busy</h5>
                    <ul class="browser-default">
                        <li>Use "QRL?" or "Is this frequency in use?" before calling</li>
                        <li>Move slightly up or down the band if needed</li>
                        <li>Try different bands if your preferred one is congested</li>
                        <li>Consider operating during off-peak hours</li>
                    </ul>
                </div>
                <div class="col s12 m6">
                    <h5>When Signal Conditions Are Poor</h5>
                    <ul class="browser-default">
                        <li>Speak slowly and enunciate clearly</li>
                        <li>Use phonetics for all parts of callsigns</li>
                        <li>Break down address information into smaller segments</li>
                        <li>Ask for confirmation of critical information</li>
                        <li>Consider switching to a digital mode if voice is difficult</li>
                    </ul>
                </div>
            </div>
        </div>
    </div>

