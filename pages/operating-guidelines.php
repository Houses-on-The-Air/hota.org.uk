    <h2>Operating Guidelines for Houses on the Air</h2>
    <p>Last updated: <?= date('F j, Y'); ?></p>

    <p>Welcome to the Houses on the Air (HOTA) operating guidelines. These guidelines are designed to ensure a smooth and enjoyable experience for all participants. Please read them carefully before participating in any HOTA activities.</p>

    <h3>General Conduct</h3>
    <ul>
        <li>Respect all participants and their property.</li>
        <li>Follow all local laws and regulations.</li>
        <li>Operate within the designated frequencies and power limits.</li>
        <li>Maintain a courteous and professional demeanor at all times.</li>
    </ul>

    <h3>Equipment and Setup</h3>
    <ul>
        <li>Ensure your equipment is in good working condition.</li>
        <li>Use appropriate antennas and power sources.</li>
        <li>Set up your station in a safe and secure location.</li>
        <li>Test your equipment before starting operations.</li>
    </ul>

    <h3>Operating Procedures</h3>
    <ul>
        <li>Identify your station clearly and frequently.</li>
        <li>Log all contacts accurately and promptly.</li>
        <li>Follow established calling and response protocols.</li>
        <li>Be prepared to provide information about your location and setup.</li>
    </ul>

    <h3>Safety and Emergency Procedures</h3>
    <ul>
        <li>Have a first aid kit and emergency contact information on hand.</li>
        <li>Be aware of your surroundings and potential hazards.</li>
        <li>Know the location of the nearest medical facilities.</li>
        <li>In case of emergency, follow established emergency procedures.</li>
    </ul>

    <h3>Feedback and Reporting</h3>
    <p>We value your feedback and encourage you to report any issues or concerns. Please contact us at <a href="mailto:info@hota.org.uk">info@hota.org.uk</a> with your comments and suggestions.</p>

    <h3>Changes to These Guidelines</h3>
    <p>We may update these guidelines from time to time. Any changes will be posted on this page, and where appropriate, notified to you by email.</p>
