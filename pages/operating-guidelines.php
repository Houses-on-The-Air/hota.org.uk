                    <p><strong>You (G3XYZ):</strong> "Thanks for the 5 and 7 report. You are 5 and 9 here in Manchester. Could you please confirm that address is 123 High Street, London SW1A 1AA?"</p>
                    <p><strong>Activator M0ABC:</strong> "That's correct, 123 High Street, London SW1A 1AA. Thanks for the contact, 73."</p>
