    <!-- Hunter Guidelines -->
    <div id="hunter-guidelines" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">search</i>Guidelines for Hunters</span>

            <p>When hunting for HOTA activations, these guidelines will help you make successful contacts:</p>

            <div class="row">
                <div class="col s12 m6">
                    <h4>Finding Activations</h4>
                    <ul class="browser-default">
                        <li>Check the HOTA Discord for announced activations</li>
                        <li>Monitor common HOTA frequencies (see <a href="?page=band-plans">Band Plans</a>)</li>
                        <li>Listen for "CQ HOTA" calls</li>
                        <li>Use spotting networks to find active stations</li>
                        <li>Be active during HOTA contests and events</li>
                    </ul>
                </div>
                <div class="col s12 m6">
                    <h4>Making Contact</h4>
                    <ul class="browser-default">
                        <li>Wait for the activator to finish their current contact</li>
                        <li>Give your callsign clearly, using phonetics once</li>
                        <li>Be patient if the frequency is busy</li>
                        <li>Listen carefully for the address information</li>
                        <li>Confirm the address if you're unsure (repeat back)</li>
                        <li>Keep your transmission brief to allow others to contact the activator</li>
                    </ul>
                </div>
            </div>

            <h4>Record Keeping</h4>
            <div class="row">
                <div class="col s12">
                    <p>When logging HOTA contacts as a hunter:</p>
                    <ol>
                        <li>Record the complete address provided by the activator</li>
                        <li>Note the date and time (UTC) accurately</li>
                        <li>Record the band, mode, and signal reports</li>
                        <li>If the address wasn't clear, request it again or check with the operator later</li>
                        <li>Consider using recording software (where legal) to ensure you capture address details correctly</li>
                    </ol>
                </div>
            </div>

            <div class="example-box">
