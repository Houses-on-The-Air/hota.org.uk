            <h4 class="mt-3">Award Rules</h4>
            <ol class="rule-list">
                <li>
                    <h5>Unique Addresses</h5>
                    <p>Awards are based on unique addresses, not contacts. Multiple contacts at the same address count only once.</p>
                </li>

                <li>
                    <h5>Address Validation</h5>
                    <p>Our system validates addresses to ensure uniqueness. Minor variations in how an address is written will be normalized during processing.</p>
                </li>

                <li>
                    <h5>Separate Award Tracks</h5>
                    <p>Activator and Hunter awards are tracked separately. You can earn awards in both categories.</p>
                </li>

                <li>
                    <h5>Automatic Processing</h5>
                    <p>Awards are automatically processed when you submit qualifying logs. You'll be notified when you reach a new award tier.</p>
                </li>

                <li>
                    <h5>Physical Certificates</h5>
                    <p>For award levels that include physical certificates, there may be a nominal fee to cover printing and shipping costs.</p>
                </li>

                <li>
                    <h5>Log Verification</h5>
                    <p>The HOTA Committee reserves the right to verify logs for award qualification. In rare cases, additional documentation may be requested.</p>
                </li>
            </ol>

