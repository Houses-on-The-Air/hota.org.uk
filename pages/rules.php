            <span class="card-title"><i class="material-icons left">new_releases</i>Special Rules & Exceptions</span>

            <h4>Contest Operations</h4>
            <p>During official HOTA contests, special rules may apply. These will be published separately for each contest event.</p>

            <h4>Multiple Operators</h4>
            <p>For multi-operator stations, all operators must be present at the house location. One operator must be designated as the responsible person to ensure compliance with all rules.</p>

            <h4>Remote Operation</h4>
            <p>Remote operation is not permitted for HOTA activations. The operator must be physically present at the house location.</p>

            <h4>Digital Mode Operations</h4>
            <p>For digital modes with limited character space (like FT8), special provisions apply for address exchange. See the <a href="?page=operating-guidelines#digital-operation">Digital Operation Guidelines</a> for details.</p>

