    <!-- General Rules -->
    <div id="general-rules" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">gavel</i>General Rules</span>

            <p>The following rules apply to all HOTA participants:</p>

            <ol class="rule-list">
                <li>
                    <h5>Legal Operation</h5>
                    <p>All operations must comply with the operator's license conditions and local regulations.</p>
                </li>

                <li>
                    <h5>Permission Required</h5>
                    <p>Operators must have proper permission to operate from any house location. This includes your own home, properties owned by family/friends (with permission), or public/historic houses where amateur radio operation is permitted.</p>
                </li>

                <li>
                    <h5>Residential Building Requirement</h5>
                    <p>HOTA operations must take place from residential buildings with fixed addresses. These include houses, apartments, condos, historic residences, etc. See the "Eligible Houses" section for detailed criteria.</p>
                </li>

                <li>
                    <h5>Safety First</h5>
                    <p>Safety is paramount. Never compromise safety for an activation. All equipment must be set up and operated safely.</p>
                </li>

                <li>
                    <h5>Address Exchange</h5>
                    <p>The complete address of the house must be provided during each contact. This information is essential for the HOTA program.</p>
                </li>

                <li>
                    <h5>Valid Contact Requirements</h5>
                    <p>A valid HOTA contact requires the exchange of callsigns, signal reports, and the complete address of at least one station. Both parties must confirm receipt of this information.</p>
                </li>

                <li>
                    <h5>Ethics and Sportsmanship</h5>
                    <p>Operators are expected to maintain the highest standards of ethics and sportsmanship. Fraudulent logging, misrepresentation of location, or other deceptive practices will result in disqualification from awards.</p>
                </li>
            </ol>

            <div class="card-panel teal lighten-5 mt-3">
                <h5><i class="material-icons left">lightbulb</i>Key Principle</h5>
                <p>HOTA is founded on the principle of trust within the amateur radio community. While we don't require photographic evidence for most operations, the Committee reserves the right to request verification of operations if questions arise.</p>
            </div>
        </div>
    </div>

    <!-- Activator Rules -->
    <div id="activator-rules" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">home</i>Activator Rules</span>

            <p>These rules apply specifically to HOTA activators (operators transmitting from house locations):</p>

            <ol class="rule-list">
                <li>
                    <h5>Station Location</h5>
                    <p>The entire station (operator, radio, and antenna) must be located within the property boundaries of the house being activated. Antennas can extend onto adjacent property only with permission from that property owner.</p>
                </li>

                <li>
                    <h5>Minimum Contacts</h5>
                    <p>A valid HOTA activation requires a minimum of 1 confirmed contact. However, we encourage activators to make at least 10 contacts from each location to give more chasers the opportunity to participate.</p>
                </li>

                <li>
                    <h5>Address Communication</h5>
                    <p>Activators must provide the complete address of the house during each QSO. This address must be accurate and specific enough to uniquely identify the house.</p>
                    <div class="example-box">
                        <strong>Acceptable:</strong> "123 High Street, Birmingham, B1 1AA, UK"<br>
                        <strong>Not Acceptable:</strong> "A house in Birmingham"
                    </div>
                </li>

                <li>
                    <h5>Portable Operation Identifier</h5>
                    <p>When operating portable, use the appropriate portable identifier according to your license regulations (e.g., /P, /M0ABC, etc.).</p>
                </li>

                <li>
                    <h5>Proximity Rule</h5>
                    <p>Multiple activations by the same operator on the same day must be from houses at least 500 meters apart, unless they are part of a designated historic complex or group housing with separate, unique addresses.</p>
                </li>

                <li>
                    <h5>Multiple Activators</h5>
                    <p>Multiple operators may activate the same house simultaneously using different callsigns and equipment. Each operator logs their contacts independently.</p>
                </li>

                <li>
                    <h5>Power Source</h5>
                    <p>There are no restrictions on the power source used. Mains power, batteries, solar, generator, etc., are all acceptable.</p>
                </li>
            </ol>

            <div class="card-panel amber lighten-5 mt-3">
                <h5><i class="material-icons left">priority_high</i>Important Note</h5>
                <p>Activators are responsible for maintaining detailed logs with accurate address information. Without the complete address in your logs, your contacts won't count for HOTA awards.</p>
            </div>
        </div>
    </div>

    <!-- Hunter Rules -->
    <div id="hunter-rules" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">search</i>Hunter Rules</span>

            <p>These rules apply to HOTA hunters (operators contacting HOTA activators):</p>

            <ol class="rule-list">
                <li>
                    <h5>Location Requirements</h5>
                    <p>Hunters may operate from any location - fixed, portable, or mobile. Unlike activators, hunters are not required to operate from house locations.</p>
                </li>

                <li>
                    <h5>Information Exchange</h5>
                    <p>Hunters must exchange callsigns and signal reports with activators and must clearly receive and acknowledge the complete address of the activator's location.</p>
                </li>

                <li>
                    <h5>Address Logging</h5>
                    <p>Hunters must log the complete address provided by the activator. This is critical for award qualification.</p>
                </li>

                <li>
                    <h5>Multiple QSOs</h5>
                    <p>Multiple QSOs with the same activator at the same address count only once for award purposes. However, if the activator moves to a different house location, a new QSO will count separately.</p>
                </li>

                <li>
                    <h5>Address Verification</h5>
                    <p>If unsure about the address details, hunters should request clarification during the QSO. Address information can also be verified via QRZ.com, HOTA Discord, or email after the contact.</p>
                </li>
            </ol>

            <div class="example-box mt-3">
                <h5>Example Hunter Exchange</h5>
                <div class="example-dialog">
                    <p><strong>Activator M0ABC:</strong> "G3XYZ you are 5 and 7 from 123 High Street, London SW1A 1AA."</p>
                    <p><strong>Hunter G3XYZ:</strong> "Thanks for the 5 and 7 report. You are 5 and 9 here in Manchester. Could you please confirm that address is 123 High Street, London SW1A 1AA?"</p>
                    <p><strong>Activator M0ABC:</strong> "That's correct, 123 High Street, London SW1A 1AA. Thanks for the contact, 73."</p>
                    <p><strong>Hunter G3XYZ:</strong> "Thanks for the activation and the address confirmation. 73."</p>
                </div>
            </div>
        </div>
