    <h2>Rules for Houses on the Air</h2>
    <p>Last updated: <?= date('F j, Y'); ?></p>

    <h3>Eligibility</h3>
    <p>To participate in the Houses on the Air program, your house must meet the following criteria:</p>
    <ul>
        <li>Be located in a residential area.</li>
        <li>Have a valid address and be registered with the local authorities.</li>
        <li>Have a suitable location for setting up an amateur radio station.</li>
        <li>Be open to sharing information about your house and location.</li>
    </ul>

    <h3>Registration</h3>
    <p>Registration is not required. However you can contact us to:</p>
    <ul>
        <li>Providing your contact information.</li>
        <li>Submit a description and photos of your house.</li>
        <li>Indicate the date and time you will be available for contact.</li>
    </ul>

    <h3>Event Guidelines</h3>
    <p>During the event, participants must adhere to the following guidelines:</p>
    <ul>
        <li>Ensure your house is clean and presentable.</li>
        <li>Be available to answer questions from contactors.</li>
        <li>Respect the privacy and property of your neighbours.</li>
        <li>Follow all local laws and regulations.</li>
    </ul>

    <h3>Code of Conduct</h3>
    <p>All participants are expected to behave in a respectful and professional manner. This includes:</p>
    <ul>
        <li>Treating all visitors with courtesy and respect.</li>
        <li>Refraining from any form of harassment or discrimination.</li>
        <li>Maintaining a safe and welcoming environment for everyone.</li>
    </ul>

    <h3>Contact Information</h3>
    <p>If you have any questions or need further information, please contact us at <a href="mailto:info@hota.org.uk">info@hota.org.uk</a>. We are here to assist you and ensure a successful event.</p>

    <h3>Changes to These Rules</h3>
    <p>We may update these rules from time to time. Any changes will be posted on this page, and where appropriate, notified to you by email.</p>
</section>
