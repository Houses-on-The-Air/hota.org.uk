    <!-- Logging Rules -->
    <div id="logging-rules" class="card mt-4">
        <div class="card-content">
            <span class="card-title"><i class="material-icons left">description</i>Logging Requirements</span>

            <p>Accurate logging is essential for HOTA participation. All logs submitted for HOTA awards must meet these requirements:</p>

            <ol class="rule-list">
                <li>
                    <h5>Format</h5>
                    <p>Logs must be submitted in ADIF format (.adi or .adif file extension). See our <a href="?page=adif-guide">ADIF Guide</a> for details.</p>
                </li>

                <li>
                    <h5>Essential Fields</h5>
                    <p>Each QSO record must include:</p>
                    <ul class="browser-default">
                        <li>Date (YYYYMMDD format)</li>
                        <li>Time (HHMMSS in UTC)</li>
                        <li>Callsign of the contacted station</li>
                        <li>Band or frequency</li>
                        <li>Mode</li>
                        <li>Signal reports</li>
                        <li>ADDRESS field with the complete house address</li>
                    </ul>
                </li>

                <li>
                    <h5>ADDRESS Field</h5>
                    <p>The ADDRESS field is critical for HOTA. It must contain the complete house address with sufficient detail to uniquely identify the location.</p>
                    <div class="example-box">
                        <strong>ADIF Example:</strong><br>
                        <code>&lt;ADDRESS:32&gt;42 Abbey Road, London, NW8 9AY, UK</code>
                    </div>
                </li>

                <li>
                    <h5>Submission Deadline</h5>
                    <p>Logs should be submitted within 30 days of the activity. While older logs will be accepted, prompt submission is encouraged.</p>
                </li>

                <li>
                    <h5>Log Accuracy</h5>
                    <p>Logs with significant errors or missing required information may be rejected. Corrections can be submitted if issues are identified.</p>
                </li>

                <li>
                    <h5>Duplicate Checking</h5>
                    <p>The HOTA system automatically checks for duplicate contacts. Submitting the same contact multiple times will not increase your award counts.</p>
                </li>
            </ol>

            <div class="card-panel blue lighten-5 mt-3">
                <h5><i class="material-icons left">tips_and_updates</i>Logging Tips</h5>
                <p>Many logging programs don't include the ADDRESS field by default. You may need to use the NOTES, COMMENT, or QTH field during logging and map this to the ADDRESS field when exporting your ADIF file.</p>
                <p>See our <a href="?page=logging-software">Software Guide</a> for specific instructions for popular logging programs.</p>
            </div>
        </div>
    </div>

