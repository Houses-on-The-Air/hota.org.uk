            <ul class="collapsible">
                <li>
                    <div class="collapsible-header"><i class="material-icons">question_answer</i>I can't connect to the TGIF network. What should I check?</div>
                    <div class="collapsible-body">
                        <p>Common connection issues include:</p>
                        <ul class="browser-default">
                            <li>Verify your internet connection is stable and working</li>
                            <li>Check that your DMR ID is properly registered and configured</li>
                            <li>Ensure your hotspot or repeater is correctly configured for TGIF</li>
                            <li>Confirm you're using the correct color code for your local repeater</li>
                            <li>Try a different device or connection method</li>
                        </ul>
                    </div>
                </li>
                <li>
                    <div class="collapsible-header"><i class="material-icons">question_answer</i>Do I need a DMR ID to use the HOTA talkgroup?</div>
                    <div class="collapsible-body">
                        <p>Yes, you need a registered DMR ID to access the TGIF network. You can get one for free from <a href="https://www.radioid.net" target="_blank" rel="nofollow">RadioID.net</a> by providing your amateur radio callsign and basic information. Your DMR ID is a unique number that identifies you on the DMR network worldwide.</p>
                    </div>
                </li>
                <li>
                    <div class="collapsible-header"><i class="material-icons">question_answer</i>Can I use my analog radio with the TGIF talkgroup?</div>
                    <div class="collapsible-body">
                        <p>No, you need a DMR-capable radio to access DMR talkgroups. Analog FM radios are not compatible with digital DMR. However, some dual-mode radios support both analog FM and DMR digital modes.</p>
                    </div>
                </li>
                <li>
                    <div class="collapsible-header"><i class="material-icons">question_answer</i>Is there a cost to use the TGIF network?</div>
                    <div class="collapsible-body">
                        <p>The TGIF network is free to use. You only need the appropriate equipment (DMR radio, hotspot, etc.) and an internet connection. There are no subscription fees for basic use of the network.</p>
                    </div>
                </li>
                <li>
                    <div class="collapsible-header"><i class="material-icons">question_answer</i>Can I record and share net sessions?</div>
                    <div class="collapsible-body">
                        <p>Yes, with proper notification. If you plan to record a session on the talkgroup for sharing online or other purposes, please announce at the beginning that you are recording. This gives participants the opportunity to opt-out if they prefer not to be recorded.</p>
                    </div>
                </li>
            </ul>
