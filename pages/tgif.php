                    <ul class="browser-default">
                        <li>Check in when the net controller calls for participants</li>
                        <li>Keep check-ins brief: callsign, name, and location</li>
                        <li>Wait to be acknowledged by the net controller</li>
                        <li>Stay on topic for the themed part of the net</li>
                        <li>Support newcomers and answer questions when appropriate</li>
                        <li>Follow directions from the net controller</li>
                        <li>Signal when you need to leave the net</li>
